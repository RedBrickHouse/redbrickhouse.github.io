function setLang(lang) {
  const t = translations[lang];
  if (!t) return;
  document.documentElement.lang = lang === 'ko' ? 'ko' : lang === 'zh' ? 'zh-CN' : 'en';
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else {
        el.innerHTML = t[key];
      }
    }
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key] !== undefined) el.placeholder = t[key];
  });
  ['phase1Li','phase2Li','phase3Li','phase4Li'].forEach(key => {
    if (!t[key]) return;
    const items = document.querySelectorAll(`[data-i18n-list="${key}"] li`);
    items.forEach((li, i) => { if (t[key][i]) li.textContent = t[key][i]; });
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  localStorage.setItem('bh-lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('bh-lang') || 'ko';
  setLang(saved);
});
